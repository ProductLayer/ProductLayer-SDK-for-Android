package com.productlayer.android.common.view;

import android.content.Context;
import android.graphics.Rect;
import android.support.v7.widget.AppCompatAutoCompleteTextView;
import android.util.AttributeSet;
import android.view.View;

/**
 * An auto-autocompletion textview that starts showing suggestions on focus when text is yet to be entered.
 */
public class FocusAutoCompleteTextView extends AppCompatAutoCompleteTextView {

    public FocusAutoCompleteTextView(Context context) {
        super(context);
    }

    public FocusAutoCompleteTextView(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    public FocusAutoCompleteTextView(Context context, AttributeSet attrs, int defStyleAttr) {
        super(context, attrs, defStyleAttr);
    }

    @Override
    public boolean enoughToFilter() {
        return true;
    }

    @Override
    protected void onFocusChanged(boolean focused, int direction, Rect previouslyFocusedRect) {
        super.onFocusChanged(focused, direction, previouslyFocusedRect);
        if (getWindowVisibility() == View.GONE) {
            return;
        }
        if (focused && getAdapter() != null) {
            performFiltering(getText(), 0);
            showDropDown();
        }
    }
}
